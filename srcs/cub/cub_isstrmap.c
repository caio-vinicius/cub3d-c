#include "cub.h"

int		cub_isstrmap(char *s)
{
	int i;

	i = 0;
	if (!s || s[i] == '\0')
		return (0);
	while (s[i])
	{
		if (s[i] == '0' || s[i] == '1' || s[i] == '2'
			|| s[i] == 'N' || s[i] == 'S' || s[i] == 'E' || s[i] == 'W'
			|| s[i] == ' ')
			i++;
		else
			return (0);
	}
	return (1);
}
